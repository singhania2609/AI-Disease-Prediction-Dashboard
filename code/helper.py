"""Helpers: PDF report generation + explainability."""
from fpdf import FPDF
from datetime import datetime
from pathlib import Path
import io


HEALTH_TIPS = {
    "Diabetes": ["Maintain healthy weight", "Limit refined sugar", "Exercise 30 min daily"],
    "Heart Disease": ["Quit smoking", "Reduce sodium", "Monitor blood pressure"],
    "Parkinson's": ["Daily physiotherapy", "Balanced diet rich in fiber", "Sleep hygiene"],
    "Liver Disease": ["Avoid alcohol", "Hydrate well", "Avoid self-medication"],
    "Hepatitis": ["Get vaccinated (Hep A/B)", "Avoid raw shellfish", "Practice safe hygiene"],
    "Lung Cancer": ["Stop smoking immediately", "Avoid pollutants", "Annual screening"],
    "Chronic Kidney Disease": ["Limit salt", "Control diabetes/BP", "Stay hydrated"],
    "Breast Cancer": ["Monthly self-exams", "Annual mammogram >40", "Limit alcohol"],
    "Jaundice": ["Hydrate", "Avoid alcohol/fatty foods", "Consult hepatologist"],
    "Symptom-Based": ["Consult a physician", "Track symptoms daily", "Rest & hydrate"],
}


def explain_prediction(disease: str, features: dict, prediction_positive: bool):
    """Simple rule-based explainability — highlights risk-driving inputs."""
    rules = {
        "Diabetes": [
            ("Glucose", lambda v: v > 140, "High glucose level"),
            ("BMI", lambda v: v > 30, "High BMI (obesity range)"),
            ("Age", lambda v: v > 45, "Age above 45"),
            ("BloodPressure", lambda v: v > 130, "Elevated blood pressure"),
        ],
        "Heart Disease": [
            ("cp", lambda v: v >= 2, "Chest pain type indicating risk"),
            ("chol", lambda v: v > 240, "High cholesterol"),
            ("trestbps", lambda v: v > 140, "High resting blood pressure"),
            ("thalach", lambda v: v < 120, "Low max heart rate"),
            ("age", lambda v: v > 55, "Age above 55"),
        ],
        "Liver Disease": [
            ("Total_Bilirubin", lambda v: v > 1.2, "Elevated bilirubin"),
            ("Alkaline_Phosphotase", lambda v: v > 200, "High alkaline phosphatase"),
            ("Alamine_Aminotransferase", lambda v: v > 50, "Elevated ALT"),
        ],
        "Chronic Kidney Disease": [
            ("bp", lambda v: v > 130, "High blood pressure"),
            ("bgr", lambda v: v > 140, "High blood glucose"),
            ("hemo", lambda v: v < 12, "Low hemoglobin"),
        ],
    }
    reasons = []
    for name, check, msg in rules.get(disease, []):
        v = features.get(name)
        try:
            if v is not None and check(float(v)):
                reasons.append(msg)
        except Exception:
            pass
    if not reasons:
        reasons = ["Combined input pattern matched the model's risk profile."] if prediction_positive \
                  else ["Inputs are within typical normal ranges."]
    return reasons


class PDFReport(FPDF):
    def header(self):
        self.set_fill_color(30, 90, 160)
        self.rect(0, 0, 210, 30, "F")
        self.set_font("Helvetica", "B", 18)
        self.set_text_color(255, 255, 255)
        self.set_xy(0, 8)
        self.cell(210, 14, "Medical Prediction Report", align="C")
        self.ln(22)
        self.set_text_color(20, 20, 20)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(120, 120, 120)
        self.cell(0, 10, f"Page {self.page_no()} - Generated by MDPS", align="C")


def generate_pdf(*, username, disease, prediction, params: dict,
                 severity=None, description=None, precautions=None,
                 explanation=None, confidence=None) -> bytes:
    pdf = PDFReport()
    pdf.set_margins(left=15, top=10, right=15)
    pdf.add_page()
    pdf.set_font("Helvetica", "", 10)

    def section(title):
        pdf.ln(3)
        pdf.set_font("Helvetica", "B", 11)
        pdf.set_text_color(30, 90, 160)
        pdf.set_x(15)
        pdf.cell(0, 8, title, new_x="LMARGIN", new_y="NEXT")
        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(20, 20, 20)

    def kv(k, v):
        # Truncate long values so they never overflow
        text = str(v)[:100]
        pdf.set_x(15)
        pdf.set_font("Helvetica", "B", 10)
        # Label in fixed 50pt column
        pdf.cell(50, 6, f"{k}:", border=0)
        pdf.set_font("Helvetica", "", 10)
        # Value fills remaining width — multi_cell resets x so we set it each time
        pdf.multi_cell(0, 6, text, border=0)
        pdf.set_x(15)

    section("Patient Info")
    kv("Name", username)
    kv("Date", datetime.now().strftime("%Y-%m-%d %H:%M"))
    kv("Disease Module", disease)

    section("Prediction Result")
    kv("Result", prediction)
    if confidence is not None:
        kv("Confidence", f"{confidence * 100:.1f}%")
    if severity is not None:
        kv("Severity Score", f"{severity:.2f}")

    section("Inputs / Symptoms")
    for k, v in params.items():
        kv(str(k), str(v))

    if description:
        section("Disease Description")
        pdf.set_x(15)
        pdf.multi_cell(0, 6, str(description)[:500])

    if explanation:
        section("Why this prediction")
        for r in explanation:
            pdf.set_x(15)
            pdf.multi_cell(0, 6, f"- {str(r)[:120]}")

    if precautions:
        section("Precautions")
        for p in precautions:
            pdf.set_x(15)
            pdf.multi_cell(0, 6, f"- {str(p)[:120]}")

    section("Health Recommendations")
    for tip in HEALTH_TIPS.get(disease, HEALTH_TIPS["Symptom-Based"]):
        pdf.set_x(15)
        pdf.multi_cell(0, 6, f"- {tip}")

    pdf.ln(4)
    pdf.set_font("Helvetica", "I", 9)
    pdf.set_text_color(150, 30, 30)
    pdf.set_x(15)
    pdf.multi_cell(0, 5,
        "Disclaimer: This report is generated by an AI model for informational purposes "
        "only and does not constitute medical advice. Please consult a qualified physician.")

    out = pdf.output(dest="S")
    if isinstance(out, str):
        out = out.encode("latin-1")
    return bytes(out)